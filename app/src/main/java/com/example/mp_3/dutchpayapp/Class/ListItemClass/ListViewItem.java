package com.example.mp_3.dutchpayapp.Class.ListItemClass;

public class ListViewItem {

    private String date;
    private String shop;
    private String mamber;
    private String cost;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getShop() {
        return shop;
    }

    public void setShop(String shop) {
        this.shop = shop;
    }

    public String getMamber() {
        return mamber;
    }

    public void setMamber(String mamber) {
        this.mamber = mamber;
    }

    public String getCost() {
        return cost;
    }

    public void setCost(String cost) {
        this.cost = cost;
    }
}
